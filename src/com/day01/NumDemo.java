package com.day01;

public class NumDemo {
    public static void main(String[] args) {
        int a=13;
        int b=4;
//        默认用int运算
        int c=a/b;//3
        float d=a/b;//3.0
        System.out.println(c);
        System.out.println(d);
        float a1=13.0f;
        System.out.println(a1/4);
    }
}
